local function read16(off)
  local lo = emu.read(off, emu.memType.snesPrgRom)
  local hi = emu.read(off + 1, emu.memType.snesPrgRom)
  return lo + hi * 256
end

local function check()
  local bankInc = read16(0x0CE600)
  for _, c in ipairs(cases) do
    local ptr = read16(0x0CE602 + 2 * c.id)
    local base = (c.id < bankInc) and 0x0D0000 or 0x0E0000
    for i, v in ipairs(c.bytes) do
      local b = emu.read(base + ptr + i - 1, emu.memType.snesPrgRom)
      if b ~= v then
        emu.log(string.format("school: dlg %d MISMATCH at +%d: got %02X want %02X (ptr=%04X bankInc=%d)",
          c.id, i - 1, b or -1, v, ptr, bankInc))
        emu.stop(1)
        return
      end
    end
    emu.log(string.format("school: dlg %d ok (%d bytes at %06X)", c.id, #c.bytes, base + ptr))
  end
  emu.log("school: OT6 lessons found in the Beginner's House - PASS")
  emu.stop(0)
end

check()
